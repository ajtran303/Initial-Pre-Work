// Functions are typically defined with one or more parameters. Parameters are placeholders for values that can be input into the function. The actual values used when a function is called are called arguments.
// Example
function ourFunctionWithArgs(a, b) {
  console.log(a - b);
}
ourFunctionWithArgs(10, 5); // Outputs 5

