// Use the .pop() function to remove the last item from an array. The element removed will be returned to be used somewhere else.

// Example
var ourArray = [1,2,3];
var removedFromOurArray = ourArray.pop();
// removedFromOurArray now equals 3, and ourArray now equals [1,2]

