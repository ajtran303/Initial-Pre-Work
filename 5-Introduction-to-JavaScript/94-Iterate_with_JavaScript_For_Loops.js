// A for loop runs for a specific number of times. It can use three optional expressions.

// for ([initialization]; [condition]; [final-expression])

// The initialization statement  executes only one time before the loop.

// The condition statement will loop while it is true. When the condition is false, it will stop. If the condition starts at false, it will never loop.

// final-expression executes at the end of each loop before the next condition check. usually used to increment or decrement a counter.

// Example
var ourArray = [];

for (var i = 0; i < 5; i++) {
  ourArray.push(i);
}

